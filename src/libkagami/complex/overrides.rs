#[derive(Clone, PartialEq)]
pub enum ASSOverride {
    Bold(bool),
    Italic(bool),
    Underline(bool),
    Strikeout(bool),
    Bord(f32),
    Shad(f32),
    Fn(String),
    Fs(f32),
    Fsp(f32),
    Blur(f32),
    Be(f32),
    Fscx(f32),
    Fscy(f32),
    Fsc(f32),
    Xbord(f32),
    Ybord(f32),
    Xshad(f32),
    Yshad(f32),
    Fax(f32),
    Fay(f32),
    Frx(f32),
    Fry(f32),
    Frz(f32),
    Fad(f32, f32),
    Fade(f32, f32, f32, f32, f32, f32, f32),
    TransformI(Vec<ASSOverride>),
    TransformII(f32, Vec<ASSOverride>),
    TransformIII(f32, f32, Vec<ASSOverride>),
    TransformIV(f32, f32, f32, Vec<ASSOverride>),
    Pos(f32, f32),
    Alpha(u32),
    AlphaI(u32),
    AlphaII(u32),
    AlphaIII(u32),
    AlphaIV(u32),
    ColorI(u32),
    ColorII(u32),
    ColorIII(u32),
    ColorIV(u32),
    An(u8),
    P(u8),
    ClipI(String),
    ClipII(f32, String),
    IclipI(String),
    IclipII(f32, String),
    Fe(f32),
    MoveI(f32, f32, f32, f32),           // \move(x1,y1,x2,y2)
    MoveII(f32, f32, f32, f32, f32, f32), // \move(x1,y1,x2,y2,t1,t2)
    Org(f32, f32),
    Pbo(f32),
    Fr(f32),                              // alias for frz, can normalize to Frz on parse
    Q(u8),
    R(Option<String>),                    // None = reset to default, Some = named style
    K(u32),
    KSweep(u32),                          // \K
    Kf(u32),
    Ko(u32),
}
